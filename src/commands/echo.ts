import { getEchoMessage } from '../messages.js';
import type { CommandContext } from '../types.js';
import { handleError } from '../utils.js';
import { validateInputLength, isSafeString, sanitizeText } from '../utils.js';

/**
 * /echo ??????????
 */
export async function handleEchoCommand({ command, ack, respond }: CommandContext): Promise<void> {
  try {
    await ack();
    
    // ??????
    const inputText = command.text || '';
    
    // ??????????
    if (!isSafeString(inputText)) {
      await respond({
        text: '???: ??????????????????????????????????',
        response_type: 'ephemeral',
      });
      return;
    }

    // ??????
    if (!validateInputLength(inputText, 1000)) {
      await respond({
        text: '???: ??????????????1000????',
        response_type: 'ephemeral',
      });
      return;
    }

    // ???????????????
    const sanitizedText = sanitizeText(inputText);
    
    await respond({
      text: getEchoMessage(sanitizedText),
      response_type: 'in_channel',
    });
  } catch (error) {
    handleError(error instanceof Error ? error : new Error(String(error)), 'Echo Command');
    // ??????????????????????
    try {
      await respond({
        text: '???????????????????????????????',
        response_type: 'ephemeral',
      });
    } catch {
      // ?????????????????
    }
  }
}
