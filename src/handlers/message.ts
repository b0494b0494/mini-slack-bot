import { detectMessageType } from '../utils.js';
import { getMentionResponseMessage } from '../messages.js';
import type { MessageContext } from '../types.js';
import { handleError, validateInputLength, isSafeString, validateUserId } from '../utils.js';

/**
 * ???????????????
 */
export async function handleMessage({ message, say }: MessageContext): Promise<void> {
  try {
    // ??????????????
    if (message.subtype || (message as { bot_id?: string }).bot_id) {
      return;
    }

    // ??????????????????
    if (!('text' in message) || !message.text) {
      return;
    }

    // ????ID????????
    if (!('user' in message) || !message.user) {
      return;
    }

    // ??????????
    if (!isSafeString(message.text)) {
      return; // ?????????????
    }

    // ??????
    if (!validateInputLength(message.text, 2000)) {
      return; // ????????????
    }

    // ????ID???
    if (!validateUserId(message.user)) {
      return; // ???????ID???
    }

    const messageType = detectMessageType(message.text);
    
    // ???????????????
    if (messageType === 'unknown') {
      return;
    }

    await say({
      text: getMentionResponseMessage(message.user, messageType),
      thread_ts: message.ts,
    });
  } catch (error) {
    handleError(error instanceof Error ? error : new Error(String(error)), 'Message Handler');
    // ????????????????????????
  }
}
